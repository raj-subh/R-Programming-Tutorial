# Try to create variable in R
# Variables store data and are created with an assignment operator.

# Naming conventions:
# Must start with a letter.
# Can include letters, numbers, underscores, and dots.

my_var <- 10
print(my_var)

.my_new_var <- my_var
print(.my_new_var)

my_new_var <- .my_new_var
print(my_new_var)
